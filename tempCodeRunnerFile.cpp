#include <iostream>
using namespace std;
int main()
{
    int a = 8, b = 3;
    // Addition
    cout << "a + b = " << a + b << endl;
    // Subtraction
    cout << "a - b = " << a - b << endl;
    // Multiplication
    cout << "a * b = " << a * b << endl;
    // Division
    cout << "a / b = " << a / b << endl;
    // Modulus
    cout << "a % b = " << a % b << endl;
}