#include <iostream>
using namespace std;
int main()
{
    int a = 6, b = 4;
    // Equal to
    cout << "a == b = " << (a == b) << endl;
    // Less than
    cout << "a < b = " << (a < b) << endl;
    // Greater than
    cout << "a > b = " << (a > b) << endl;
    // Less than or equal to
    cout << "a <= b = " << (a <= b) << endl;
    // Greater than or equal to
    cout << "a >= b = " << (a >= b) << endl;
    // Not equal to
    cout << "a != b = " << (a != b) << endl;
    return 0;
}